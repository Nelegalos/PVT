package by.pvt.resource.exception;

@SuppressWarnings("serial")
public class OperatorTechnicalException extends Exception {

	

	public OperatorTechnicalException() {
		super();
	}

	public OperatorTechnicalException(String message, Throwable cause) {
		super(message, cause);
	}

	public OperatorTechnicalException(String message) {
		super(message);
	}

	public OperatorTechnicalException(Throwable cause) {
		super(cause);
	}
}